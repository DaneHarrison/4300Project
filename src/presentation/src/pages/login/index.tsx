/*
   LoginPage
   Login page where user face is authenticated 
   name is then displayed under the button
*/

import { type NextPage } from "next";
import Head from "next/head";
import { useState } from "react";
import Matches from "../../../ components/matches";
import SignIn from "../../../ components/signin";

/*
   LoginPage
   Login page to submit user image
*/
const LoginPage: NextPage = () => {
  const [resultImage, setResultImages] = useState(null);
  const [resultName, setResultName] = useState("");

  // changeImage
  // image accepts param of image string
  // changes image state
  const changeImage = (image: any) => {
    setResultImages(image);
  };

  // changeName
  // changes name state
  // name param is the name from the server response
  const changeName = (name: string) => {
    setResultName(name);
  };
  return (
    <div>
      <Head>
        <title>MOB system</title>
        <meta name="description" content="Generated by create-t3-app" />
        <link rel="icon" href="/favicon.ico" />
      </Head>
      <div className="flex h-screen flex-col items-center justify-center">
        <SignIn changeImage={changeImage} changeName={changeName} />
        <Matches name={resultName} images={resultImage} />
      </div>
    </div>
  );
};

export default LoginPage;